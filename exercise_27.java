package sass;
public class exercise_27 {

    public static void main(String[] args) {
        
        String value_to_convert = "10";
        System.out.println("value in string: " + value_to_convert);

        int converted_to_int = Integer.parseInt(value_to_convert);
        System.out.println("value in int: " + converted_to_int);

        float converted_to_float = Float.parseFloat(value_to_convert);
        System.out.println("value in float: " + converted_to_float);


    }
}


